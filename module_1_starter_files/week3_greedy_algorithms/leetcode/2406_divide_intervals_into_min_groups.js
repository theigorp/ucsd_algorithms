function divideIntervalsIntoMinNumberOfGroups(intervals) {
  const groups = [];
  //* O(nlogn)
  intervals.sort((a, b) => a[0] - b[0]);
  //* O(n^2)
  for (const interval of intervals) {
    const nonIntersectingGroup = groups.find((group) => group[group.length - 1][1] < interval[0]);
    if (nonIntersectingGroup) {
      nonIntersectingGroup.push(interval);
    } else {
      groups.push([interval]);
    }
  }

  return groups;
}

